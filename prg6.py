# Write a program that creates a nested list and then accesses a specific element in the list. Print the accessed element.
# Create a nested list
nested_list = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Access a specific element (e.g., the element in the second row, third column)
# In Python, indexing starts from 0, so the second row is at index 1, and the third column is at index 2.
accessed_element = nested_list[1][2]

# Print the accessed element
print("Accessed element:", accessed_element)
